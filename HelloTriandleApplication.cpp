void HelloTriandleApplication::run() {
    while (!glfwWindowShouldClose(_window)) {
        glfwPollEvents();
        tick();
    }
    clean();
}

void HelloTriandleApplication::initVulkan() {
    createInstance();
    setDebugCallback();
    createSurface();
    pickPhysicalDevice();
    createLogicalDevice();
    createSwapChain();
    createImageViews();
    createGraphicsPipeline();
}

